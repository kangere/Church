public class JSONParser {
    static InputStream is = null;
    static JSONObject jObj = null;
    static String json = "";

    // constructor
    public JSONParser() {

    }

    /** Method posts or gets data from the url specified, and returns
     * a JSONObject
     *
     *
     * @param url
     * @param method
     * @param params
     * @return - returns jsonbject containing data specified by the Url
     */
    public JSONObject makeHttpRequest(String url, String method,
                                      List<NameValuePair> params) {


        // Making HTTP request
        try {

            URL my_url = new URL(url);
            HttpURLConnection conn = (HttpURLConnection) my_url.openConnection();

            // check for request method
            if(method == "POST"){
                // request method is POST
                // defaultHttpClient deprecated
                DefaultHttpClient httpClient = new DefaultHttpClient();
                HttpPost httpPost = new HttpPost(url);
                httpPost.setEntity(new UrlEncodedFormEntity(params));

                HttpResponse httpResponse = httpClient.execute(httpPost);
                HttpEntity httpEntity = httpResponse.getEntity();
                is = httpEntity.getContent();

                //httpurlconnection code
                /*conn.setDoOutput(true);

                conn.connect();*/
                //conn.setRequestProperty();
                /*DataOutputStream outputStream = new DataOutputStream(conn.getOutputStream());
                outputStream.writeBytes(params.toString());*/

            }else if(method == "GET"){

                // old deprecated code
                /*DefaultHttpClient httpClient = new DefaultHttpClient();
                String paramString = URLEncodedUtils.format(params, "utf-8");
                url += "?" + paramString;
                HttpGet httpGet = new HttpGet(url);

                HttpResponse httpResponse = httpClient.execute(httpGet);
                HttpEntity httpEntity = httpResponse.getEntity();
                is = httpEntity.getContent();*/


                conn.setReadTimeout(10000);
                conn.setConnectTimeout(15000);              //maximum time to wait while connecting
                conn.setRequestMethod(method);
                //testing url connection code
                conn.setDoInput(true);

                //start query
                conn.connect();
                int response = conn.getResponseCode();      //get response code from connection
                Log.d("JSON GET RESPONSE","The response is: " + response);
                is = conn.getInputStream();



            }

            //close connection
            conn.disconnect();

        } catch (UnsupportedEncodingException e) {
            e.printStackTrace();
        } catch(IOException e){
            e.printStackTrace();
        }

        try {
            BufferedReader reader = new BufferedReader(new InputStreamReader(
                    is, "iso-8859-1"), 8);
            StringBuilder sb = new StringBuilder();
            String line;
            while ((line = reader.readLine()) != null) {
                sb.append(line + "\n");
            }
            is.close();
            json = sb.toString();
        } catch (Exception e) {
            Log.e("Buffer Error", "Error converting result " + e.toString());
        }

        // try parse the string to a JSON object
        try {
            jObj = new JSONObject(json);
        } catch (JSONException e) {
            Log.e("JSON Parser", "Error parsing data " + e.toString());
        }


        // return JSON String
        return jObj;

    }
}
